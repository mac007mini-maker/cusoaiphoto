  void _submitFeedback(BuildContext dialogContext) async {
    final feedback = _model.feedbackController?.text ?? '';
    final email = _model.emailController?.text ?? '';

    if (feedback.isEmpty) {
      ScaffoldMessenger.of(context).showSnackBar(
        SnackBar(
          content: Text('Please describe what you would like us to improve'),
          backgroundColor: Colors.red,
        ),
      );
      return;
    }

    final Uri emailUri = Uri(
      scheme: 'mailto',
      path: kSupportEmail,
      queryParameters: {
        'subject': 'Viso AI Feedback',
        'body': 'Feedback: $feedback\n\nFrom: $email',
      },
    );

    try {
      if (await canLaunchUrl(emailUri)) {
        await launchUrl(emailUri);
        _model.feedbackController?.clear();
        _model.emailController?.clear();
        
        Navigator.pop(dialogContext);
        
        if (mounted) {
          ScaffoldMessenger.of(context).showSnackBar(
            SnackBar(
              content: Text('Thank you for your feedback!'),
              backgroundColor: Color(0xFF10B981),
            ),
          );
        }
      } else {
        throw 'Could not launch email client';
      }
    } catch (e) {
      Navigator.pop(dialogContext);
      
      if (mounted) {
        ScaffoldMessenger.of(context).showSnackBar(
          SnackBar(
            content: Text('Failed to send feedback. Please try again.'),
            backgroundColor: Colors.red,
          ),
        );
      }
    }
  }

